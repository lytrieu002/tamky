function thongke_init() {
    $bt_kb = (isset($_POST['bt_kb']))?esc_attr($_POST['bt_kb']) : '';
	$bt_huyen = (isset($_POST['bt_huyen']))?esc_attr($_POST['bt_huyen']) : '';
	if($bt_huyen){
		$bt_huyenar = "AND objName2='$bt_huyen'";
	} else {
		$bt_huyenar = "";
	}
	$bt_xa = (isset($_POST['bt_xa']))?esc_attr($_POST['bt_xa']) : '';
	if($bt_xa){
		$bt_xaar = "AND objName='$bt_xa'";
	} else {
		$bt_xaar = "";
	}

	//$selectedScenario = $_POST['selectedScenario'];
	global $wpdb;
	$flood_result = $wpdb->get_results("SELECT scenario, objName, objName2, objCode, SUM(objArea) as objArea2 FROM wp_flood_data WHERE scenario='$bt_kb' $bt_huyenar $bt_xaar GROUP BY objName, objCode", 'ARRAY_A');

	ob_start(); //bắt đầu bộ nhớ đệm
	?>
	<table style="border-collapse: collapse;margin-left: auto; margin-right: auto;font-size: 0.9em;font-family: sans-serif;min-width: 480px;max-width: 1000px;box-shadow: 0 0 20px rgba(0, 0, 0, 0.15);width: 100%;">
	  <thead>
		  <tr style="text-align: center;background-color: #009879;color: #ffffff;">
			  <th style="width: 100px;padding: 12px 15px;">Kịch bản</th>
				<th style="width: 300px;padding: 12px 15px;">Huyện</th>
			  <th style="width: 250px;padding: 12px 15px;">Xã</th>
			  <th style="width: 250px;padding: 12px 15px;">Mức ngập</th>
			  <th style="width: 150px;padding: 12px 15px;">Diện tích ngập (km2)</th>
		  </tr>
	  </thead>
	  <tbody>
		<?php
		$x = 0;
		foreach ($flood_result as $key => $marker) {
			if ($x % 2 == 0){ ?>
				<tr  style="padding: 12px 15px;border-bottom: 1px solid #dddddd;background-color: #f3f3f3;">
			<?php }
			else{?>
				<tr  style="padding: 12px 15px;border-bottom: 1px solid #dddddd;border-bottom: 2px solid #009879;">
			<?php }
			$x = $x + 1;
			?>
			<td><?php echo $marker['scenario'] ?></td>
			<td><?php echo $marker['objName2'] ?></td>
			<td><?php echo $marker['objName'] ?></td>
			<td><?php
				switch ($marker['objCode']){
					case "1":
						echo 'H < 0.5m';
						break;
					case "2":
						echo '0.5m < H < 1.0m';
						break;
					default:
						echo 'H > 1.0m';
				}
			?></td>
			<td><?php echo round($marker['objArea2']/1000000,3) ?></td>
			</tr>
		<?php }; ?>
	  </tbody>
	  </table>
    <?php $result = ob_get_clean(); //cho hết bộ nhớ đệm vào biến $result
    wp_send_json_success($result);
    die();//bắt buộc phải có khi kết thúc
} 
